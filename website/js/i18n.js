const RTL_LANGS = ["ar"];

function setLang(lang) {
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = RTL_LANGS.includes(lang) ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  if (dict.meta_title) document.title = dict.meta_title;
  try { localStorage.setItem("etr_lang", lang); } catch (e) {}
  const sel = document.getElementById("lang-select");
  if (sel) sel.value = lang;
}

document.addEventListener("DOMContentLoaded", () => {
  let saved = null;
  try { saved = localStorage.getItem("etr_lang"); } catch (e) {}
  const browser = (navigator.language || "en").slice(0, 2);
  const initial = I18N[saved] ? saved : (I18N[browser] ? browser : "en");
  const sel = document.getElementById("lang-select");
  if (sel) sel.addEventListener("change", (e) => setLang(e.target.value));
  setLang(initial);
});
